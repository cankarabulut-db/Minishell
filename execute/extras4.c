#include "../minishell.h"

void	duplicate_open_close(int *fd, int a)
{
	if (a == 1)
	{
		fd[0] = dup(0);
		fd[1] = dup(1);
	}
	else if (a == 2)
	{
		dup2(fd[0], 0);
		close(fd[0]);
		dup2(fd[1], 1);
		close(fd[1]);
	}
}
